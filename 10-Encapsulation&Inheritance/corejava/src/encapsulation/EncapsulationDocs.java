package encapsulation;

public class EncapsulationDocs {
	/*
	 * ----------- Encapsulation ------------
	 * 
	 * # process of data hiding(class ko property lai bahira nadekhau) and data binding
	 * # to achieve encapsulation:
	 * 		a> Declare properties of class as private(i.e. data hiding)
	 * 		b> Create public getters and setters methods to get and set values to private properties
	 * 			(data binding)
	 * 
	 * # Syntax:
	 * 	
	 * 		// POJO or Model or Entity class(Business Logic navako, plain data matrai vako)
	 * 
	 * 		class class_name{
	 * 			// private properties
	 * 
	 * 			// public getters and setters methods
	 * 		}
	 * 
	 */
}
