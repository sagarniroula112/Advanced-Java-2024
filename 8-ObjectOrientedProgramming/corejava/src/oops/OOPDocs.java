package oops;

public class OOPDocs {
	/*
	 * ------- OOP(Object Oriented Programming) --------
	 * 
	 *  -> Structure real-world scenario
	 *  -> Features discussion:
	 *  		- Class and Object
	 *  		- Encapsulation
	 *  		- Inheritance
	 *  		- Polymorphism
	 *  		- Abstraction
	 *  			a> Abstract class
	 *  			b> Interface
	 *  
	 *  -> Class : It is used to represent real world entity/things.
	 *  		   It contains properties and methods.
	 *  		   It's a template or blueprint of object. (conceptual visualization)
	 *  		   It is a collection/group of objects having similar behaviors.
	 * 			   It is a user defined or reference data type. (e.g. Employee)
	 * 
	 * 		# Syntax:
	 * 
	 * 				class class_name{
	 * 					// properties
	 * 
	 * 					// methods
	 * 				}
	 * 
	 *   -> Object : Implementation of class is an object.
	 *   			 Instance of class. (object of class= instance)
	 *   			 Object contains instance variable and instance methods.
	 *   			 Object allocates memory when it is created.
	 *   
	 *   	# Syntax:
	 *   
	 *   			class_name obj_name = new class_name();
	 *   
	 *   							OR
	 *   
	 *   					new class_name();
	 *   
	 */
}
